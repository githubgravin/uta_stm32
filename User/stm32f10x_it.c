/* 串口1接收中断 */
void USART1_IRQHandler(void)
{
   //uint8_t rec_cmd;
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)
	{     
			ch = USART_ReceiveData(USART1);
		
      printf("%c \r\n", ch);
 
	}	 
}

// EXTI Line --> PF9 触摸屏中断
void EXTI9_5_IRQHandler(void)
{ 
  if(EXTI_GetITStatus(EXTI_Line9) != RESET)
  {	
    touch_flag=1;

		EXTI_ClearITPendingBit(EXTI_Line9);
  }
}





